def func(num1, num2):
    """Calculates an average between 2 numbers.
       :param num1: number 1 value
       :param num2: number 2 value
       :type num1: float
       :type num2: float
       :return: The result of average of num1 and num2
       :rtype: float
       """
    return (num1 + num2) / 2

def main():
    help(func)

if __name__ == '__main__':
    main()